t9_ru = r"""**Фотоэффект и квантовые свойства света**
Фотоэффект (фотоэлектрический эффект) — это явление вырывания электронов из вещества (чаще всего металлов) под действием падающего света. Он был открыт в 1887 году Генрихом Герцем, а в 1905 году Альберт Эйнштейн объяснил его на основе квантовой теории, за что получил Нобелевскую премию.

Фотоэффект доказывает корпускулярную (квантовую) природу света. Свет представляет собой поток частиц, называемых фотонами.
Уравнение Эйнштейна для фотоэффекта (закон сохранения энергии):
\(h\nu = A + \frac{mv^2}{2}\)
где:
\(h\nu\) — энергия поглощенного фотона (\(h\) — постоянная Планка);
\(A\) — работа выхода электрона из металла;
\(\frac{mv^2}{2}\) — максимальная кинетическая энергия вылетевшего электрона.

Законы фотоэффекта:
1. Максимальная кинетическая энергия фотоэлектронов не зависит от интенсивности света, а линейно зависит от его частоты.
2. Для каждого вещества существует «красная граница» фотоэффекта (минимальная частота или максимальная длина волны), ниже которой фотоэффект не происходит.

Применение: Солнечные батареи, фотоэлементы, датчики освещенности, автоматические двери, приборы ночного видения."""

