# Topic 4: Көз / Глаз
t4_kz = """Көз — адамның көру мүшесі, ол күрделі оптикалық жүйе болып табылады.

Негізгі бөліктері:
- Қабықша (роговица) — жарықты алғашқы сындырушы.
- Қарашық (зрачок) — жарық ағынын реттеуші.
- Көз бұршағы (хрусталик) — фокусты реттейтін тірі линза.
- Торлама (сетчатка) — кескін пайда болатын жарық сезгіш қабық.

Көру кемістіктері:
1. Жақыннан көргіштік — кескін торламаның алдында түзіледі. Шашыратқыш линзамен (теріс) түзеледі.
2. Алыстан көргіштік — кескін торламаның артында түзіледі. Жинағыш линзамен (оң) түзеледі."""

t4_ru = """Глаз — орган зрения человека, представляющий собой сложную оптическую систему.

Основные части:
- Роговица — передняя прозрачная часть, сильно преломляющая свет.
- Зрачок — отверстие, регулирующее количество поступающего света.
- Хрусталик — биологическая линза, меняющая свою кривизну (аккомодация) для фокусировки.
- Сетчатка — светочувствительная оболочка, на которой формируется изображение.

Недостатки зрения:
1. Близорукость — изображение формируется перед сетчаткой. Исправляется рассеивающими (отрицательными) линзами.
2. Дальнозоркость — изображение формируется за сетчаткой. Исправляется собирающими (положительными) линзами."""

# Topic 9: Фотоэффект / Фотоэффект
t9_kz = """Фотоэффект — жарықтың әсерінен заттан (көбіне металдардан) электрондардың ұшып шығу құбылысы.

Оны 1887 жылы Г. Герц ашты, ал А. Эйнштейн 1905 жылы кванттық теория негізінде түсіндірді.

Эйнштейн теңдеуі: hν = A + E, мұндағы hν — фотон энергиясы, A — шығу жұмысы, E — электронның кинетикалық энергиясы.

Қолданылуы: күн батареялары, фотоэлементтер, түнгі көру құралдары."""

t9_ru = """Фотоэффект — это явление вырывания электронов из вещества (обычно металлов) под действием падающего света.

Открыт Г. Герцем в 1887 году, а теоретически объяснен А. Эйнштейном в 1905 году на основе квантовых представлений.

Уравнение Эйнштейна: hν = A + E, где hν — энергия фотона, A — работа выхода электрона, E — кинетическая энергия вылетевшего электрона.

Применение: солнечные батареи, фотоэлементы, датчики движения, приборы ночного видения."""

# Topic 10: Лазерлер / Лазеры
t10_kz = """Лазер — қуатты, бағытталған және когерентті жарық сәулесін шығаратын кванттық генератор.

Лазер сәулесінің қасиеттері:
1. Монохроматтылық (тек бір түсті).
2. Жоғары бағыттылық (таралмайды).
3. Когеренттілік.
4. Үлкен энергия тығыздығы.

Қолданылуы: медицина (ота жасау), өнеркәсіп (кесу, пісіру), байланыс (оптикалық талшықтар), ғылыми зерттеулер."""

t10_ru = """Лазер — это квантовый генератор, испускающий узконаправленный, монохроматический и когерентный луч света.

Свойства лазерного луча:
1. Монохроматичность (строго определенная длина волны/цвет).
2. Высокая направленность (луч почти не расширяется).
3. Когерентность.
4. Огромная плотность энергии.

Применение: медицина (лазерная хирургия, коррекция зрения), промышленность (резка металлов), связь (оптоволокно), считывание штрих-кодов."""

def update_topics():
    topics_content = [
        (4, t4_kz, t4_ru),
        (9, t9_kz, t9_ru),
        (10, t10_kz, t10_ru),
    ]
    
    for order, kz, ru in topics_content:
        topic = Topic.objects.filter(order=order).first()
        if topic:
            topic.content_kz = kz
            topic.content_ru = ru
            topic.save()
            print(f"Updated Topic {order}")
        else:
            print(f"Topic {order} not found")

if __name__ == "__main__":
    update_topics()
