def update_topics():
    topics_content = [
        (5, t5_kz, t5_ru),
        (6, t6_kz, t6_ru),
        (7, t7_kz, t7_ru),
        (8, t8_kz, t8_ru),
    ]
    
    for order, kz, ru in topics_content:
        topic = Topic.objects.filter(order=order).first()
        if topic:
            topic.content_kz = kz
            topic.content_ru = ru
            topic.save()
            print(f"Updated Topic {order}")
        else:
            print(f"Topic {order} not found")

if __name__ == "__main__":
    update_topics()
